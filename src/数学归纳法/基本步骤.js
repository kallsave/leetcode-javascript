// 证明: 0到n的整数之和和n * (n + 1) / 2相等

// 步骤一: 基底的证明
// 当n = 0时, 左右两边等式成立

// 步骤二: 假设任意k
// 当假设任意一个整数k, 0 + 1 + ... + k = k * (k + 1) / 2

// 步骤三: 证明k + 1左右等式成立
// 所以0 + 1 + ... + (k + 1) = (k + 1) * ((k + 1) + 1) / 2

function f (k) {
  return k * (k + 1) / 2
}

f(k + 1) === f(k) + k + 1 === k * (k + 1) / 2 + k + 1 === (k + 1) * ((k + 1) + 1) / 2
f(k + 1) === (k + 1) * ((k + 1) + 1) / 2

// 左边 === 右边, 所以证明成立


